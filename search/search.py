import os

from dotenv import load_dotenv
from openai import OpenAI

from integrations.sheets import get_worksheet

load_dotenv()

client = OpenAI(
    api_key=os.getenv("OPENAI_API_KEY")
)


def build_video_cards(rows: list[dict]) -> str:
    cards = []

    for index, row in enumerate(rows, start=3):
        card = f"""
Строка: {index}
Название файла: {row.get("file_name", "")}
Папка: {row.get("folder", "")}
Длительность ролика в секундах: {row.get("duration_sec", "")}

Транскрибация:
{row.get("transcript_text", "")}

Транскрибация с таймкодами:
{row.get("transcript_segments", "")}

OCR / текст на экране:
{row.get("ocr_text", "")}

OCR с обложки:
{row.get("poster_ocr_text", "")}

Vision / описание сцен:
{row.get("scene_description", "")}

Vision / объекты:
{row.get("visible_objects", "")}

Vision / действия:
{row.get("scene_actions", "")}

Vision / темы:
{row.get("visual_topics", "")}

Vision / поисковые фразы:
{row.get("search_phrases", "")}
"""
        cards.append(card.strip())

    return "\n\n---\n\n".join(cards)


def search_video(query: str) -> str:
    sheet = get_worksheet()
    rows = sheet.get_all_records(head=2)

    video_cards = build_video_cards(rows)

    prompt = f"""
Ты — AI-поиск по видеоархиву.

Твоя задача — найти наиболее подходящие ролики по запросу пользователя.

У тебя есть карточки роликов из Google Sheets. В карточках могут быть:
- название файла;
- папка;
- длительность ролика в секундах;
- транскрибация речи;
- транскрибация с таймкодами;
- OCR-текст с экрана;
- OCR-текст с обложки;
- Vision-описание сцен;
- объекты, действия, темы и поисковые фразы.

Используй всю информацию из карточек для поиска наиболее релевантного ролика.

Правила:
1. Ищи не только по точному совпадению слов, но и по смыслу.
2. Если найден один уверенный результат — верни только его.
3. Если есть несколько одинаковых роликов в разных папках — верни все найденные варианты.
4. Если пользователь указывает дополнительные характеристики (например длительность, автора, фамилию, примерное название, папку или другие уточнения), обязательно учитывай их при ранжировании.
5. Если пользователь указывает длительность ролика, используй только значение из поля «Длительность ролика в секундах». Не оценивай длительность самостоятельно и не используй длительность отдельных фрагментов вместо длительности ролика.
6. Поле «Длительность ролика в секундах» используй только для анализа. Никогда не выводи его пользователю.
7. Чем больше условий из запроса совпадает с карточкой ролика, тем выше должен быть результат.
8. Если один результат значительно лучше соответствует всем условиям запроса, не добавляй менее подходящие варианты.
9. Если подходящих роликов нет — честно напиши, что ничего не найдено.
10. Не выдумывай данные, которых нет в карточках.
11. Ответ должен быть коротким, понятным и удобным для чтения в Telegram.
12. В ответе запрещено добавлять любые пояснения, причины выбора, комментарии или описание содержания ролика.
13. Ответ должен содержать только поля из формата ответа: строка, название, папка.

Формат ответа:

Строка: ...
Название: ...
Папка: ...

Запрос пользователя:
{query}

Карточки роликов:
{video_cards}
"""

    response = client.responses.create(
        model="gpt-4.1-mini",
        input=prompt
    )

    return response.output_text